        '$env:NEXT_PUBLIC_AI_WORKER_URL = ''http://localhost:' + $script:Ports.AI_WORKER_PORT + '''; ' +
        'npm run dev -- --port ' + $script:Ports.FRONTEND_PORT

    $loggedCommand = New-LoggedCommand $frontendCommand $frontendLog
    $process = Start-Process powershell `
        -WorkingDirectory $FrontendDir `
        -ArgumentList @("-NoProfile", "-ExecutionPolicy", "Bypass", "-Command", $loggedCommand) `
        -PassThru `
