function Remove-AnsiCodes([string]$Text) {
    return $Text -replace "`e\[[0-9;]*[A-Za-z]", ""
}

function Invoke-DockerComposePsJson {
    $startInfo = [System.Diagnostics.ProcessStartInfo]::new()
    $startInfo.FileName = "docker"
    $startInfo.Arguments = "compose -f `"$ComposeFile`" ps --format json"
    $startInfo.RedirectStandardOutput = $true
    $startInfo.RedirectStandardError = $true
    $startInfo.UseShellExecute = $false

    $process = [System.Diagnostics.Process]::Start($startInfo)
    $stdout = $process.StandardOutput.ReadToEnd()
    $process.StandardError.ReadToEnd() | Out-Null
    $process.WaitForExit()

    if ($process.ExitCode -ne 0) {
        return $null
    }

    return $stdout
}

function Wait-InfraReady([int]$TimeoutSeconds) {
    $deadline = (Get-Date).AddSeconds($TimeoutSeconds)
    while ((Get-Date) -lt $deadline) {
        $statusJson = Invoke-DockerComposePsJson
        if ($statusJson) {
            $services = @($statusJson -split "`r?`n" | Where-Object { $_.Trim() } | ForEach-Object { $_ | ConvertFrom-Json })
            if ($services.Count -ge 6) {
                $notReady = @($services | Where-Object {
                    $_.State -ne "running" -or ($_.Health -and $_.Health -ne "healthy")
                })
                if ($notReady.Count -eq 0) {
                    return $true
                }
            }
        }
        Start-Sleep -Seconds 2
    }
    return $false
}

function Start-Infra {
    $dockerLog = Join-Path $LogsDir "docker.log"
    Write-Host "Starting Docker infrastructure..." -ForegroundColor Cyan
    docker compose -f $ComposeFile up -d | Tee-Object -FilePath $dockerLog
    if (-not (Wait-InfraReady 90)) {
        "`n=== Docker Compose Status: TIMEOUT waiting for all services healthy ===" | Out-File -FilePath $dockerLog -Append -Encoding utf8
    }
    else {
        "`n=== Docker Compose Status: all services running/healthy ===" | Out-File -FilePath $dockerLog -Append -Encoding utf8
    }
    docker compose -f $ComposeFile ps | Out-File -FilePath $dockerLog -Append -Encoding utf8
    "`n=== Docker Compose Logs ===" | Out-File -FilePath $dockerLog -Append -Encoding utf8
    $composeLogs = docker compose -f $ComposeFile logs --no-color
    (Remove-AnsiCodes ($composeLogs -join "`n")) | Out-File -FilePath $dockerLog -Append -Encoding utf8
}

function Stop-Infra {
    if (Test-Path $ComposeFile) {
        Write-Host "Stopping Docker infrastructure..." -ForegroundColor Cyan
        docker compose -f $ComposeFile down
    }
}

function Clean-Infra {
    if (Test-Path $ComposeFile) {
        Write-Host "Removing Docker containers, volumes, and orphans for this compose project..." -ForegroundColor Yellow
        docker compose -f $ComposeFile down -v --remove-orphans
    }
}

