    Show-Usage
    exit 1
}

switch ($Command) {
    "up" {
        Ensure-RuntimeDirs
        Ensure-Prerequisites
        Set-RuntimePorts
        Write-PortsEnv
        Write-FrontendEnv
        Start-Infra
        Start-Backend
        Start-Frontend
        Start-AIWorker
        Write-Host "FireSafe runtime started." -ForegroundColor Green
        Write-Host "Logs: .runtime/logs/" -ForegroundColor Cyan
    }
    "down" {
        Ensure-RuntimeDirs
        Stop-RuntimeProcess "ai-worker" $AIWorkerDir
        Stop-RuntimeProcess "frontend" $FrontendDir
        Stop-RuntimeProcess "backend" $BackendDir
        Stop-Infra
        Remove-RuntimeArtifacts
        Write-Host "FireSafe runtime stopped. Runtime artifacts removed." -ForegroundColor Green
    }
    "clean" {
        Ensure-RuntimeDirs
        Stop-RuntimeProcess "ai-worker" $AIWorkerDir
        Stop-RuntimeProcess "frontend" $FrontendDir
        Stop-RuntimeProcess "backend" $BackendDir
        Clean-Infra
        Remove-BackendTarget
        Remove-AIWorkerVenv
        Remove-FrontendNodeModules
        Remove-FrontendNextBuild
        Remove-LocalJdk
        Remove-RuntimeArtifacts
        Write-Host "FireSafe runtime cleaned. Runtime artifacts removed." -ForegroundColor Green
    }
}
